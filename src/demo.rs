use crate::bus::Bus;

pub const DEMO_START_ADDR: u16 = 0x8000;
pub const DEMO_PROGRAM: [u8; 28] = [
    0xA2, 0x0A, 0x8E, 0x00, 0x00, 0xA2, 0x03, 0x8E, 0x01, 0x00, 0xAC, 0x00, 0x00, 0xA9, 0x00,
    0x18, 0x6D, 0x01, 0x00, 0x88, 0xD0, 0xFA, 0x8D, 0x02, 0x00, 0xEA, 0xEA, 0xEA,
];

pub fn load_demo_into_bus(bus: &mut Bus) {
    bus.load_program(&DEMO_PROGRAM, DEMO_START_ADDR);
    bus.set_reset_vector(DEMO_START_ADDR);
}
